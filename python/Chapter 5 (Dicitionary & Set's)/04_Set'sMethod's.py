# There Are Various Type's Of Set's Method

# add

priz = {2, 3, 45, 45}

priz.add(1)

print(priz)  # {1, 2, 3, 45}

# remove

let = {1, 34, 635, 324, 64, 654}

let.remove(1)

print(let) # {64, 34, 324, 635, 654}


# Union Method

beta = {2, 23.234, 750, "43"}

cera = {3, 34.345, 861, "54"}

print(beta.union(cera))  # {2, 34.345, 3, 750, '54', 23.234, '43', 861}

# lenght

JETA = {"""omer""", 34, 453}

print(len(JETA)) # 3

# clear

SovietUnion = {"DeadliestLeader: Joseph Stalin", "Rank: 1st", "Soilders: 30,000,000", "Nukes: 39, 000"}

SovietUnion.clear()

print(len(SovietUnion)) # 0