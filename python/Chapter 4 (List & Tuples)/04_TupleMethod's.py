# Count Method

b = (2, "322", False)

bb = b.count(1)

print(bb) # 0 {0 is liye bata raha hai kyun ke ismein 0 aik dafa bhi nahi aya hai}

# Slice Method

c = (1, "df", False, 2, 4.45, "satring")

print(c[1:3]) # ('df', False)

# Length Method


i = ("omer", 23, False, 234, 5665, 3241, 3245)

